export const normalizeText = (text: string): string => {
  return text
    .toLowerCase()
    .normalize("NFD")
    .replace(/[\u0300-\u036f]/g, "") // Remove accents
    .replace(/[^a-z0-9]/g, ""); // Remove non-alphanumeric characters
};

export const getLevenshteinDistance = (a: string, b: string): number => {
  const matrix = [];

  for (let i = 0; i <= b.length; i++) {
    matrix[i] = [i];
  }

  for (let j = 0; j <= a.length; j++) {
    matrix[0][j] = j;
  }

  for (let i = 1; i <= b.length; i++) {
    for (let j = 1; j <= a.length; j++) {
      if (b.charAt(i - 1) === a.charAt(j - 1)) {
        matrix[i][j] = matrix[i - 1][j - 1];
      } else {
        matrix[i][j] = Math.min(
          matrix[i - 1][j - 1] + 1, // substitution
          Math.min(
            matrix[i][j - 1] + 1, // insertion
            matrix[i - 1][j] + 1 // deletion
          )
        );
      }
    }
  }

  return matrix[b.length][a.length];
};

export const isCloseMatch = (guess: string, target: string, threshold: number = 2): boolean => {
  const normalizedGuess = normalizeText(guess);
  const normalizedTarget = normalizeText(target);
  
  if (Math.abs(normalizedGuess.length - normalizedTarget.length) > threshold) {
    return false;
  }

  const distance = getLevenshteinDistance(normalizedGuess, normalizedTarget);
  return distance > 0 && distance <= threshold;
};